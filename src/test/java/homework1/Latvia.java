package homework1;

public class Latvia {
    //description of or lovely country Latvia
    private double area;
    private boolean accessToTheSea;
    private int numberOfInhabitants;
    private int numberOfRivers;
    private int numberOfLakes;

    //---------SETTERS/GETTERS----------

    public double getArea() {
        return area;
    }

    public void setArea(double area) {
        this.area = area;
    }

    public boolean isAccessToTheSea() {
        return accessToTheSea;
    }

    public void setAccessToTheSea(boolean accessToTheSea) {
        this.accessToTheSea = accessToTheSea;
    }

    public int getNumberOfInhabitants() {
        return numberOfInhabitants;
    }

    public void setNumberOfInhabitants(int numberOfInhabitants) {
        this.numberOfInhabitants = numberOfInhabitants;
    }

    public int getNumberOfRivers() {
        return numberOfRivers;
    }

    public void setNumberOfRivers(int numberOfRivers) {
        this.numberOfRivers = numberOfRivers;
    }

    public int getNumberOfLakes() {
        return numberOfLakes;
    }

    public void setNumberOfLakes(int numberOfLakes) {
        this.numberOfLakes = numberOfLakes;
    }
}
